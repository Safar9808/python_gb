'''Напишите программу, которая принимает на вход координаты точки (X и Y),
   причём X ≠ 0 и Y ≠ 0 и выдаёт номер четверти плоскости,
   в которой находится эта точка (или на какой оси она находится).'''

x = int(input('Введите x точки: '))
y = int(input('Введите y точки: '))
if x == 0: print('точка на оси y ')
elif y == 0: print('точка на оси x ')
elif x > 0 and y > 0: print('точка в 1 четверти ')
elif x > 0 and y < 0: print('точка в 4 четверти ')
elif x < 0 and y > 0: print('точка в 2 четверти ')
else: print('точка в 3 четверти ')


'''Напишите программу, которая принимает на вход координаты двух точек
   и находит расстояние между ними в 2D пространстве.'''

x1, y1 = map(int, input('Введите координаты точки A через запятую: ').split(","))
x2, y2 = map(int, input('Введите координаты точки B через запятую: ').split(","))
d = ((x2-x1)**2 + (y2-y1)**2)**0.5
print('Расстояние между точками ', round(d,2))